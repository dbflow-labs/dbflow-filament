<?php

declare(strict_types=1);

namespace DbflowLabs\Filament\Tests;

use DbflowLabs\Core\Providers\DBFlowServiceProvider;
use DbflowLabs\Filament\Providers\DBFlowFilamentServiceProvider;
use Orchestra\Testbench\TestCase as OrchestraTestCase;

abstract class TestCase extends OrchestraTestCase
{
    /**
     * @param  \Illuminate\Foundation\Application  $app
     * @return list<class-string>
     */
    protected function getPackageProviders($app): array
    {
        return [
            DBFlowServiceProvider::class,
            DBFlowFilamentServiceProvider::class,
        ];
    }

    /**
     * @param  \Illuminate\Foundation\Application  $app
     */
    protected function defineEnvironment($app): void
    {
        $app['config']->set('database.default', 'testing');
        $app['config']->set('database.connections.testing', [
            'driver' => 'sqlite',
            'database' => ':memory:',
            'prefix' => '',
            'foreign_key_constraints' => true,
        ]);

        $app['config']->set('app.locale', 'en');
    }
}
